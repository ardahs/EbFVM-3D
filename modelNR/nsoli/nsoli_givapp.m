function [vrot] = nsoli_givapp(c, s, vin, k)
% *************************************************************************************************************************************************************
% Function  : Apply a sequence of k Givens rotations, used within gmres codes
%             (Kelley, C.T., Solving Nonlinear Equations with Newton's Method)
%
% *** INPUTS **************************************************************************************************************************************************
%
% *** OUTPUTS *************************************************************************************************************************************************
%
% *************************************************************************************************************************************************************
% C. T. Kelley, April 1, 2003
% This code comes with no guarantee or warranty of any kind.
% *************************************************************************************************************************************************************

    vrot = vin;
    for i = 1:k
        w1 = c(i)*vrot(i)-s(i)*vrot(i+1);
        % Here's a modest change that makes the code work in complex arithmetic. Thanks to Howard Elman for this.
        % w2 = s(i)*vrot(i)+c(i)*vrot(i+1);
        w2          = s(i)*vrot(i)+conj(c(i))*vrot(i+1);
        vrot(i:i+1) = [w1,w2];
    end
    
end % *** END FUNCTION % **************************************************************************************************************************************